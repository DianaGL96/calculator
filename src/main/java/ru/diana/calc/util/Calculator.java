package ru.diana.calc.util;

public class Calculator {
    public static int getSum (int num1, int num2) {
        return num1 + num2;
    }

    public static int getDifference (int num1, int num2) {
        return num1 - num2;
    }

    public static int getProduct (int num1, int num2) {
        return num1 * num2;
    }

}
